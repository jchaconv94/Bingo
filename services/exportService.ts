const createTempCardElement = (participant: Participant, card: BingoCard): HTMLElement => {
  const container = document.createElement('div');
  Object.assign(container.style, {
    width: '600px',
    padding: '40px',
    backgroundColor: '#f8fafc', // slate-50
    color: '#0f172a', // slate-900
    fontFamily: "'Inter', sans-serif",
    position: 'absolute',
    top: '-9999px',
    left: '-9999px',
    boxSizing: 'border-box',
  });

  // Header Section matching the "BINGO VIRTUAL" clean design
  const header = `
    <div style="display: flex; justify-content: space-between; align-items: flex-start; margin-bottom: 20px;">
      <div>
        <h1 style="font-size: 42px; font-weight: 900; margin: 0; color: #1e293b; letter-spacing: -1px; line-height: 1;">BINGO VIRTUAL</h1>
        <div style="margin-top: 12px;">
           <div style="font-size: 22px; font-weight: 700; color: #334155; line-height: 1.2;">${participant.name} ${participant.surname}</div>
           <div style="font-size: 16px; color: #64748b;">DNI: ${participant.dni}</div>
        </div>
      </div>
      <div style="text-align: right;">
        <div style="font-size: 36px; font-weight: 800; color: #0f172a;">#${card.id}</div>
        <div style="font-size: 14px; color: #94a3b8; margin-top: 4px;">${new Date().toLocaleDateString()}</div>
      </div>
    </div>
    
    <div style="border-bottom: 2px solid #e2e8f0; margin-bottom: 20px;"></div>
  `;

  // Grid Section with dark rounded cells
  let gridHtml = `
    <table style="width: 100%; border-collapse: separate; border-spacing: 10px;">
       <thead>
        <tr>
          ${['B','I','N','G','O'].map(l => 
            `<th style="
                text-align: center; 
                font-size: 42px; 
                font-weight: 900; 
                color: #475569; 
                padding-bottom: 10px;
                line-height: 1;
            ">${l}</th>`
          ).join('')}
        </tr>
      </thead>
      <tbody>
  `;

  for(let r=0; r<5; r++) {
    gridHtml += '<tr>';
    for(let c=0; c<5; c++) {
      const index = r * 5 + c;
      const value = card.numbers[index];
      const isCenter = index === 12;

      const cellStyle = `
        width: 20%;
        height: 80px;
        background-color: #1e293b; /* Dark Slate */
        color: white;
        border-radius: 12px;
        text-align: center;
        vertical-align: middle;
        font-size: 34px;
        font-weight: 700;
        padding: 0;
        margin: 0;
        line-height: 1;
        box-shadow: 0 4px 6px -1px rgba(0, 0, 0, 0.1);
      `;

      if (isCenter) {
        gridHtml += `
          <td style="${cellStyle}">
             <!-- Star Icon SVG -->
             <div style="display:flex; justify-content:center; align-items:center; height:100%; width: 100%;">
                <svg width="36" height="36" viewBox="0 0 24 24" fill="#cbd5e1" stroke="none" xmlns="http://www.w3.org/2000/svg" style="opacity: 0.5;">
                   <path d="M12 2l3.09 6.26L22 9.27l-5 4.87 1.18 6.88L12 17.77l-6.18 3.25L7 14.14 2 9.27l6.91-1.01L12 2z"/>
                </svg>
             </div>
          </td>`;
      } else {
        gridHtml += `<td style="${cellStyle}">${value}</td>`;
      }
    }
    gridHtml += '</tr>';
  }

  gridHtml += `</tbody></table>`;

  const footer = `
    <div style="margin-top: 25px; border-top: 1px dashed #cbd5e1; padding-top: 15px; text-align: center; font-size: 12px; color: #94a3b8; font-weight: 500;">
      <div>¡Buena suerte! • Sistema de Bingo Virtual</div>
      <div style="font-size: 10px; margin-top: 4px; opacity: 0.8; color: #cbd5e1;">By Owoweb</div>
    </div>
  `;

  container.innerHTML = header + gridHtml + footer;
  return container;
};

export const downloadCardImage = async (participant: Participant, card: BingoCard) => {
  const el = createTempCardElement(participant, card);
  document.body.appendChild(el);
  try {
    const canvas = await html2canvas(el, { scale: 2, useCORS: true, backgroundColor: '#f8fafc' });
    const blob = await new Promise<Blob | null>(resolve => canvas.toBlob(resolve, 'image/png'));
    if (blob) {
      saveAs(blob, `bingo_${participant.name.replace(/\s+/g,'_')}_${card.id}.png`);
    }
  } finally {
    document.body.removeChild(el);
  }
};

export const downloadAllCardsZip = async (participants: Participant[]) => {
  const zip = new JSZip();
  const folder = zip.folder("cartones_bingo");
  
  for (const p of participants) {
    for (const card of p.cards) {
      const el = createTempCardElement(p, card);
      document.body.appendChild(el);
      try {
        const canvas = await html2canvas(el, { scale: 1.5, backgroundColor: '#f8fafc' });
        const dataUrl = canvas.toDataURL('image/png');
        const base64 = dataUrl.split(',')[1];
        folder?.file(`${p.name.replace(/\s+/g,'_')}_${card.id}.png`, base64, { base64: true });
      } finally {
        document.body.removeChild(el);
      }
    }
  }

  const content = await zip.generateAsync({ type: "blob" });
  saveAs(content, "todos_cartones.zip");
};